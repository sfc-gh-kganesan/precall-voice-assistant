    po_header_wd_id text,
    created_date text,
    po_document_date text,
    issued_date text,
    approved_date text,
    submitted_by text,
    po_status text,
    po_header_cancellation_date text,
    supplier text,
    supplier_id text,
    payment_terms text,
    default_payment_terms text,
    version integer,
    change_order_reason text,
    transaction_currency text,
    total_po_amount_local real,
    total_po_amount_usd real,
    prior_version_po_amount_local real,
    prior_version_po_amount_usd real,
    net_amount_change_from_prior_version_local real,
    net_amount_change_from_prior_version_usd real,
    original_po_amount_local real,
    original_po_amount_usd real,
    net_amount_change_from_original_po_local real,
    net_amount_change_from_original_po_usd real,
    memo text,
    is_coupa integer,
    invoicing_status text,
    report_run_date text
);

