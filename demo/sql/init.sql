-- Create table for purchase order line item data.
-- Each row represents an invoice line item that was matched
-- to a purchase order.
create table purchase_order_line_item (
    po_header_number text,
    po_header_wd_id text,
    po_status text,
    po_line_number text,
    created_moment text,
    po_line_wd_id text primary key,
    pr_header_number text,
    requester text,
    is_po_line_canceled integer,
    company text,
    spend_category text,
    cost_center text,
    location text,
    engagement text,
    is_excluded integer,
    capital_project_id text,
    description text,
    is_goods integer,
    quantity real,
    unit_cost real,
    is_service integer,
    service_start_date text,
    service_end_date text,
    currency text,
    purchase_order_line_amount_local real,
    purchase_order_line_amount_usd real,
    actual_amount_invoiced_local real,
    actual_amount_invoiced_usd real,
    uninvoiced_amount_local real,
    uninvoiced_amount_usd real,
    line_payment_status text,
    is_non_cancelable integer,
    deliver_to_location text,
    ship_to_address text,
    po_line_product_type text,
    is_open_line integer,
    is_po_with_open_line integer,
    is_open_po_status integer,
    report_run_date text
);

